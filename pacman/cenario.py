    def pintar_pontos(self, tela):
        pontos_x = 30 * self.tamanho
        img_pontos = constantes.FONTE.render(f"Score: {self.pontos}", True, cores.AMARELO)
        tela.blit(img_pontos, (pontos_x, 50))

    def pintar_linha(self, numero_linha, linha, tela):
        for numero_coluna, coluna in enumerate(linha):
            x = numero_coluna * self.tamanho
            y = numero_linha * self.tamanho
            metade_do_tamanho = self.tamanho // 2
            cor = cores.PRETO
            if coluna == 2:
                cor = cores.AZUL
            pygame.draw.rect(tela, cor, (x, y, self.tamanho, self.tamanho), 0)
            if coluna == 1:
                pygame.draw.circle(tela, cores.AMARELO, (x + metade_do_tamanho, y + metade_do_tamanho),
                                   self.tamanho // 10, 0)

    def pintar(self, tela):
        for numero_linha, linha in enumerate(self.matriz):
            self.pintar_linha(numero_linha, linha, tela)
        self.pintar_pontos(tela)

    def calcular_regras(self):
        direcoes = self.get_direcoes(self.fantasma.linha, self.fantasma.coluna)
        col = self.pacman.coluna_intencao
        lin = self.pacman.linha_intencao
        if 0 <= col < 28 and 0 <= lin < 29:
            if self.matriz[lin][col] != 2:
                self.pacman.aceitar_movimento()
                if self.matriz[lin][col] == 1:
                    self.pontos += 1
                    self.matriz[lin][col] = 0

    def get_direcoes(self, linha, coluna):
        direcoes = []
        if self.matriz[int(linha - 1)][int(coluna)] != 2:
            direcoes.append(constantes.CIMA)
        if self.matriz[int(linha + 1)][int(coluna)]:
            direcoes.append(constantes.BAIXO)
        if self.matriz[int(linha)][int(coluna - 1)]:
            direcoes.append(constantes.ESQUERDA)
        if self.matriz[int(linha)][int(coluna + 1)]:
            direcoes.append(constantes.DIREITA)
        return direcoes

    def processar_eventos(self, even):
        for e in even:
            if e.type == pygame.QUIT:
                exit()
